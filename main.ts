sprites.onOverlap(SpriteKind.Player, SpriteKind.Object, function (sprite, otherSprite) {
    info.changeScoreBy(1)
    pages.destroy()
    generatepages()
    if (info.score() == 8) {
        level2()
    }
    if (info.score() == 15) {
        game.showLongText("CONGRADULATIONS!!", DialogLayout.Bottom)
        game.over(true, effects.confetti)
    }
})
